<p>
<b>Student Gymkhana:</b> The student gymkhana of IITP is the governing and representative body of students responsible to head and manage all the student activities of the institute. Secretaries are appointed through common vote amon students for the posts of General, Cultural, Technical, Welfare, Mantainance, Environmental and Mess Secretaries. 
