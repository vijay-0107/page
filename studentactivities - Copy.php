<b>Science and Technology Council:</b> The STC-IITP organizes numerous workshops, seminars and competitions in areas of student interest such as Robotics, Bio-mechanics, Ethical Hacking, Microelectronics to name a few. Competitions in circuit building and debugging, programming, mechanical constructions etc are regularly held throughout the term.  
</p>
<p>
<b>Cultural Club:</b> The club manages all the cultural aspects of the campus like celebrating festivals and organizing competitions in singing and dancing. It also conducts regular shows of campus rock bands and street dancing.   
</p>
<p>
<b>Literary Club:</b>This club organizes activities to develop and showcase the literary prowess of students of IITP in various competitions like arbit speaking, writing, elocutions, treasure hunts, dramatics etc.The club's members have also won debates against law veterans from other colleges.
</p>
<p>
<b>Environmental Club:</b>This handles the responsibility of having a green and an environmentally sound campus and creating awareness among the students by organising competitions aiming at development of alternative and clean fuels for the future together with effecient utilisation of renewable energy.
</p>
<p>
<h2>ORGANISATIONAL ACTIVITIES</h2>

The students organize cultural, literary and technical events throughout the calendar. These festivals and functions are an effort on the part of the students to strengthen the camarederie between students, continue the rich cultural heritage of India, showcase and pitch technical talent against students from other institute and learn valuable lessons in leadership and management qualities. The most prominent among them being:
</p>
<p>
<b>ANWESHA:</b> The annual cultural-tech fest of IITP is held over a period of 3 days in the month of february where students from the institute and other colleges take part in a wide range of technical, cultural and literary events to pitch their talents against each other. Seminars and guest lectures by prominent people from reputed institutions and top companies encourage and enrich the students.
</p>
<p>
<b>REVERBERANCE:</b> A gala evening of fun and frolic held in the month of november constitutes of cultural performances from the students coupled with miscellaneous games. An opportunity to relax and unwind for both students and faculty in the midst of a hectic academic semester.
</p>
<p>
<b>NEBULA: </b>A party organised by the seniors of IITP and held every August to welcome the new batch of freshers is an evening of performances by the  freshers and seniors alike and the informal amalgamation of the new batch into the life and culture of IITP.
</p>
